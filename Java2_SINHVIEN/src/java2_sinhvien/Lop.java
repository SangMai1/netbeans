package java2_sinhvien;

/**
 *
 * @author SANG
 */
public class Lop {
    private String id;
    private String name;

    public Lop() {
    }

    public Lop(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return this.name;
    }
    
}
