    public boolean validateForm(){
        if (txtMaCD.getText().isEmpty() || txtTenCD.getText().isEmpty() || txtHocPhi.getText().isEmpty() || txtThoiLuong.getText().isEmpty() || txtMoTa.getText().isEmpty()) {
            return false;
        }
        return true;
    }
    public ChuyenDe getModel(){
        ChuyenDe cd = new ChuyenDe();
        cd.setMaCD(txtMaCD.getText());
        cd.setTenCD(txtTenCD.getText());
        cd.setHocPhi(Double.parseDouble(txtHocPhi.getText()));
        cd.setThoiLuong(Integer.parseInt(txtThoiLuong.getText()));
        cd.setMota(txtMoTa.getText());
        if (file != null) {
            cd.setHinh(file.getName());
        } else {
            cd.setHinh("");
        }
        return cd;
    }
    public void fillData(){
        DefaultTableModel tbModel = (DefaultTableModel) tbTableChuyenDe.getModel();
        tbModel.setRowCount(0);
        for (ChuyenDe cd : cddao.getAllChuyenDe()) {
            Object[] rowData = new Object[6];
            rowData[0] = cd.getMaCD();
            rowData[1] = cd.getTenCD();
            rowData[2] = cd.getHocPhi();
            rowData[3] = cd.getThoiLuong();
            rowData[4] = cd.getMota();
            rowData[5] = cd.getHinh();
            tbModel.addRow(rowData);
         }
    }
    public ChuyenDe getAtPointChuyenDe(int pos){
        ChuyenDe cd = new ChuyenDe();
        cd.setMaCD(tbTableChuyenDe.getValueAt(pos, 0).toString());
        cd.setTenCD(tbTableChuyenDe.getValueAt(pos, 1).toString());
        cd.setHocPhi(Double.parseDouble(tbTableChuyenDe.getValueAt(pos, 2).toString()));
        cd.setThoiLuong(Integer.parseInt(tbTableChuyenDe.getValueAt(pos, 3).toString()));
        cd.setMota(tbTableChuyenDe.getValueAt(pos, 4).toString());
        
       
//        ImageIcon icon = new ImageIcon(getClass().getResource("/Hinh/"+tbTableChuyenDe.getValueAt(pos, 5).toString()));
//        icon = new ImageIcon(icon.getImage().getScaledInstance(lblHinh.getWidth(), lblHinh.getHeight(), 0));
//        lblHinh.setIcon(icon);
        cd.setHinh(tbTableChuyenDe.getValueAt(pos, 5).toString());
     
        return cd;
    }
    public void setModel(ChuyenDe cd){
        
        txtMaCD.setText(cd.getMaCD());
        txtTenCD.setText(cd.getTenCD());
        txtHocPhi.setText(String.valueOf(cd.getHocPhi()));
        txtThoiLuong.setText(String.valueOf(cd.getThoiLuong()));
        txtMoTa.setText(cd.getMota());
        System.out.println(""+cd.getHinh());
//        jButton1.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Hinh/Accept.png")));
        ImageIcon icon = new ImageIcon(getClass().getResource("/Hinh/"+cd.getHinh()));
        icon = new ImageIcon(icon.getImage().getScaledInstance(lblHinh.getWidth(), lblHinh.getHeight(), 0));
        lblHinh.setIcon(icon);
    
    }
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            ChuyenDe cd = getModel();
            if (cddao.getChuyenDeByMaSV(cd.getMaCD()) != null) {
                JOptionPane.showMessageDialog(this, "ma chuyen de nay da ton tai");
            } else {
                if (cddao.add(cd) > 0) {
                    JOptionPane.showMessageDialog(this, "add thanh cong");
                    fillData();
                }
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

