    public boolean validateForm(){
        if (txtMaNH.getText().isEmpty() || txtHoTen.getText().isEmpty() || txtDiaChiEmail.getText().isEmpty() || txtDienThoai.getText().isEmpty() || txtGhiChu.getText().isEmpty() || txtNgaySinh.getText().isEmpty()) {
            return false;
        }
        return true;
    }
    public NguoiHoc getModel() throws ParseException{
        NguoiHoc nh = new NguoiHoc();
        nh.setMaNH(txtMaNH.getText());
        nh.setHoTen(txtHoTen.getText());
        boolean gt = false;
        if (cboGioiTinh.getSelectedIndex() == 0) {
            gt = true;
        }
        nh.setGioiTinh(gt);
        nh.setNgaySinh(DateHelper.toDate(txtNgaySinh.getText()));
        nh.setDienThoai(txtDienThoai.getText());
        nh.setEmail(txtDiaChiEmail.getText());
        nh.setGhiChu(txtGhiChu.getText());
        nh.setNgayDK(DateHelper.now());
        return nh;
    }
    public void fillData(){
        DefaultTableModel tbModel = (DefaultTableModel) tbTableNguoiHoc.getModel();
        tbModel.setRowCount(0);
        for (NguoiHoc nh : nhdao.getAllNguoiHoc()) {
            Object[] rowData = new Object[8];
            rowData[0] = nh.getMaNH();
            rowData[1] = nh.getHoTen();
            String gt = "Nam";
            if (nh.isGioiTinh()) {
                gt = "Nữ";
            }
            rowData[2] = gt;
            rowData[3] = DateHelper.toString(nh.getNgaySinh());
            rowData[4] = nh.getDienThoai();
            rowData[5] = nh.getEmail();
            rowData[6] = nh.getMaNV();
            rowData[7] = nh.getNgayDK();
            tbModel.addRow(rowData);
        }
    }
    public void fillDataByMaNH(String find){
        DefaultTableModel tbModel = (DefaultTableModel) tbTableNguoiHoc.getModel();
        tbModel.setRowCount(0);
        for (NguoiHoc nh : nhdao.getNguoiHocByMaNH(find)) {
            Object[] rowData = new Object[8];
            rowData[0] = nh.getMaNH();
            rowData[1] = nh.getHoTen();
            String gt = "Nam";
            if (nh.isGioiTinh()) {
                gt = "Nữ";
            }
            rowData[2] = gt;
            rowData[3] = DateHelper.toString(nh.getNgaySinh());
            rowData[4] = nh.getDienThoai();
            rowData[5] = nh.getEmail();
            rowData[6] = nh.getMaNV();
            rowData[7] = nh.getNgayDK();
            tbModel.addRow(rowData);
        }
    }
    public void setModel(NguoiHoc nh){
        txtMaNH.setText(nh.getMaNH());
        txtHoTen.setText(nh.getHoTen());
        cboGioiTinh.setSelectedItem(nh.isGioiTinh());
        txtNgaySinh.setText(DateHelper.toString(nh.getNgaySinh()));
        txtDienThoai.setText(nh.getDienThoai());
        txtDiaChiEmail.setText(nh.getEmail());
    }
    public NguoiHoc getAtPointNguoiHoc(int pos){
        NguoiHoc nh = new NguoiHoc();
        nh.setMaNH(tbTableNguoiHoc.getValueAt(pos, 0).toString());
        nh.setHoTen(tbTableNguoiHoc.getValueAt(pos, 1).toString());
        if (tbTableNguoiHoc.getValueAt(pos, 2).toString().equalsIgnoreCase("Nam")) {
            cboGioiTinh.setSelectedIndex(0);
        } else {
            cboGioiTinh.setSelectedIndex(1);
        }
        nh.setNgaySinh(DateHelper.toDate(tbTableNguoiHoc.getValueAt(pos, 3).toString()));
        nh.setDienThoai(tbTableNguoiHoc.getValueAt(pos, 4).toString());
        nh.setEmail(tbTableNguoiHoc.getValueAt(pos, 5).toString());
        
        return nh;
    }
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            // TODO add your handling code here:
            NguoiHoc nh = getModel();
            if (nhdao.add(nh) > 0) {
                JOptionPane.showMessageDialog(this, "add thanh cong");
                fillData();
            }
        } catch (ParseException ex) {
            Logger.getLogger(QuanLiNguoiHocDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void tbTableNguoiHocMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbTableNguoiHocMousePressed
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            rowId = tbTableNguoiHoc.rowAtPoint(evt.getPoint());
            NguoiHoc nh = getAtPointNguoiHoc(rowId);
            setModel(nh);
        }
        
    }//GEN-LAST:event_tbTableNguoiHocMousePressed

