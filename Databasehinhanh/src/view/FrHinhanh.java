    private void lblHinhanhMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblHinhanhMouseClicked
        
            // TODO add your handling code here:
            JFileChooser fileChooser = new JFileChooser("C:\\Users\\SANG\\Documents\\NetBeansProjects\\Databasehinhanh\\src\\images");
            fileChooser.showOpenDialog(null);
            file = fileChooser.getSelectedFile();
            fileName = file.getAbsolutePath();
            ImageIcon imagesicon = new ImageIcon(new ImageIcon(fileName).getImage().getScaledInstance(lblHinhanh.getWidth(), lblHinhanh.getHeight(), Image.SCALE_SMOOTH));
            lblHinhanh.setIcon(imagesicon);
            try {
                    File image = new File(fileName);
                    FileInputStream fis = new FileInputStream(file);
                    ByteArrayOutputStream bos = new ByteArrayOutputStream();
                    byte[] buf = new byte[1024];
                    for (int redNum; (redNum = fis.read(buf))!=-1;){
                        bos.write(buf, 0, redNum);
                    }
                    person_images = bos.toByteArray();
        } catch (Exception e) {
                System.out.println("Error"+e.toString());
        }
        
    }//GEN-LAST:event_lblHinhanhMouseClicked
    public Hinhanh getModel(){
        Hinhanh ha = new Hinhanh();
        ha.setId(Integer.parseInt(txtId.getText()));
        if (file != null) {
            ha.setHinhanh(person_images);
        } else {
            ha.setHinhanh();
        }
        return ha;
    }
