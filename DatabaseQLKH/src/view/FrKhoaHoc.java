    public void getCombobox(){
        DefaultComboBoxModel<ChuyenDe> model = (DefaultComboBoxModel) cboChuyenDe.getModel();
        model.removeAllElements();
        chuyenDeSelect = new HashMap<>();
        List<ChuyenDe> ls = cddao.getAllChuyenDe();
        int count =0;
        for (ChuyenDe cd : ls) {
            chuyenDeSelect.put(cd.getMaCD(), count);
            model.addElement(cd);
            ++count;
        }
//        for (ChuyenDe cd : ls) {
//            model.addElement(cd);
//        }
    }

    public KhoaHoc getModel(){
        KhoaHoc kh = new KhoaHoc();
        kh.setChuyenDe(new ChuyenDe(cboChuyenDe.getSelectedItem().toString()));
        kh.setThoiLuong(Integer.parseInt(txtThoiLuong.getText()));
        kh.setHocPhi(Float.parseFloat(txtHocPhi.getText()));
        kh.setNgayKhaiGiang(DateHelper.add(10));
        kh.setNguoiTao(new NhanVien(txtNguoiTao.getText()));
        kh.setNgayTao(DateHelper.now());
        return kh;
    }
    public void fillData(){
        DefaultTableModel tbModel = (DefaultTableModel) tbTableQuanLyKhoaHoc.getModel();
        tbModel.setRowCount(0);
        for (KhoaHoc kh : khdao.getAllKhoaHoc()) {
            Object[] rowData = new Object[7];
            rowData[0] = kh.getMaKH();
            rowData[1] = kh.getChuyenDe();
            rowData[2] = kh.getThoiLuong();
            rowData[3] = kh.getHocPhi();
            rowData[4] = kh.getNgayKhaiGiang();
            rowData[5] = kh.getNguoiTao();
            rowData[6] = kh.getNgayTao();
            tbModel.addRow(rowData);
        }
    }
    public void setModel(KhoaHoc kh){
//        cboChuyenDe.setSelectedItem(kh.getChuyenDe().getMaCD());
        txtNgayKhaiGiang.setText(DateHelper.toString(kh.getNgayKhaiGiang()));
        txtHocPhi.setText(String.valueOf(kh.getHocPhi()));
        txtThoiLuong.setText(String.valueOf(kh.getThoiLuong()));
        txtNguoiTao.setText(kh.getNguoiTao().toString());
        txtNgayTao.setText(DateHelper.toString(kh.getNgayTao()));
        txtGhiChu.setText(kh.getGhiChu());
    }
    public KhoaHoc getAtPointKhoaHoc(int pos){
        KhoaHoc kh = new KhoaHoc();
        kh.setMaKH((int) tbTableQuanLyKhoaHoc.getValueAt(pos, 0));
        cboChuyenDe.setSelectedItem(cddao.getChuyenDeByMaSV(tbTableQuanLyKhoaHoc.getValueAt(pos, 1).toString()));
        kh.setThoiLuong(Integer.parseInt(tbTableQuanLyKhoaHoc.getValueAt(pos, 2).toString()));
        kh.setHocPhi(Float.parseFloat(tbTableQuanLyKhoaHoc.getValueAt(pos, 3).toString()));
        kh.setNgayKhaiGiang(DateHelper.toDate(tbTableQuanLyKhoaHoc.getValueAt(pos, 4).toString()));
        kh.setNguoiTao(new NhanVien(tbTableQuanLyKhoaHoc.getValueAt(pos, 5).toString()));
        kh.setNgayTao(DateHelper.toDate(tbTableQuanLyKhoaHoc.getValueAt(pos, 6).toString()));
        return kh;
    }
