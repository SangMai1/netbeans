    public boolean checkValition(){
        if (txtId.getText().isEmpty() || txtTenSach.getText().isEmpty() || txtTacgia.getText().isEmpty() || txtnxb.getText().isEmpty()) {
            return false;
        }
        return true;
    }
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (!checkValition()) {
            JOptionPane.showMessageDialog(this, "value is emtry");
        } else {
            int id = Integer.parseInt(txtId.getText());
            String tensach = txtTenSach.getText();
            String tacgia = txtTacgia.getText();
            String nxb = txtnxb.getText();
            String theloai = "";
            if (rdkinhdoanh.isSelected()) {
                theloai = rdkinhdoanh.getText();
            } else if (rdLaptrinh.isSelected()) {
                theloai = rdLaptrinh.getText();
            } else{
                theloai = rdSale.getText();
            }

            String sotang = cboTang.getSelectedItem().toString();
            Sach ss = new Sach(id, tensach, tacgia, nxb,theloai , sotang);
            if (sd.addSach(ss) > 0) {
                lblSatus.setText("add successly");
                lblCount.setText("COUNT:"+sd.count());
                model.addRow(new Object[]{
            ss.getId(),ss.getTensach(), ss.getTacgia(), ss.getNxb(),ss.isTheloai(), ss.getTang()
        });
             
                
            } else {
                lblSatus.setText("ERROR");
            }
        }   
    }//GEN-LAST:event_btnAddActionPerformed
