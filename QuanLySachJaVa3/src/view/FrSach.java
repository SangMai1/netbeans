    private void tbTableSachMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbTableSachMousePressed
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            row = tbTableSach.rowAtPoint(evt.getPoint());
            Sach s = getAtPoint(row);
            setModel(s);
            int mg = (int) tbTableSach.getValueAt(row,4);
            if (mg == 0) {
                cboNhaXuatBan.setSelectedItem(sachSelect.get(s.getMaxb().getTennxb()));
            }
        }
    }//GEN-LAST:event_tbTableSachMousePressed

