    public void fillData(){
        DefaultTableModel tbModel = (DefaultTableModel) tbTableMuaHang.getModel();
        tbModel.setRowCount(0);
        for (SanPham sp : lsSP) {
            Object[] rowData = new Object[5];
            tongtien = tongtien + sp.getDongia() * sp.getSoluong();
            rowData[0] = sp.getMasanpham();
            rowData[1] = sp.getTensanpham();
            rowData[2] = sp.getSoluong();
            rowData[3] = sp.getDongia();
            rowData[4] = sp.getDongia() * sp.getSoluong();
            tbModel.addRow(rowData);
        }
        txtTongBan.setText(tongtien+"");
    }
    
