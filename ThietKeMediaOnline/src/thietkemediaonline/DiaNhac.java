package thietkemediaonline;

/**
 *
 * @author SANG
 */
public class DiaNhac {
    private String NSX;
    private String casi;

    public DiaNhac() {
    }

    public DiaNhac(String NSX, String casi) {
        this.NSX = NSX;
        this.casi = casi;
    }

    public String getNSX() {
        return NSX;
    }

    public void setNSX(String NSX) {
        this.NSX = NSX;
    }

    public String getCasi() {
        return casi;
    }

    public void setCasi(String casi) {
        this.casi = casi;
    }
    
}
