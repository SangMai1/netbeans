    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        
            try {
                int id1 = tbStudent.getSelectedRow();
                StudentWeb st = setModel();
                if (id1 >= 0) {
                    tbModel.setValueAt(st.getId(), id1, 0);
                    tbModel.setValueAt(st.getName(), id1, 1);
                    tbModel.setValueAt(st.isGender(), id1, 2);
                    tbModel.setValueAt(st.getBirthay(), id1, 3);
                    tbModel.setValueAt(st.getSql(), id1, 4);
                    tbModel.setValueAt(st.getEnglish(), id1, 5);
                    tbModel.setValueAt(st.getPhp1(), id1, 6);
                    tbModel.setValueAt(st.getPhp2(), id1, 7);
                }
               
            } catch (Exception ex) {
                System.out.println("ERROR:"+ex.toString());
            }  
    }//GEN-LAST:event_btnEditActionPerformed

