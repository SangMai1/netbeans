    public SinhVien getModel(){
        SinhVien sv = new SinhVien();
        sv.setMaSinhVien(txtMaSinhVien.getText().trim());
        sv.setHotenSinhVien(txtHoTen.getText().trim());
        sv.setEmail(txtEmail.getText());
        sv.setPhone(txtPhone.getText());
        boolean gt = false;
        if (rdNam.isSelected()) {
            gt = true;
        }
        sv.setGioitinh(gt);
        sv.setDiachi(txtDiaChi.getText().trim());
        if (file != null) {
            sv.setHinhanh(file.getName());
        } else {
            sv.setHinhanh("");
        }
        return sv;
    }
    public boolean validateform(){
        StringBuilder sb = new StringBuilder();
        if (txtMaSinhVien.getText().isEmpty()) {
            sb.append("Ban chua nhap ma sinh vien \n");
            txtMaSinhVien.setBackground(Color.red);
            JOptionPane.showMessageDialog(this, sb);
            return false;
        } else {
            txtMaSinhVien.setBackground(Color.white);
        }
        if (txtHoTen.getText().isEmpty()) {
            sb.append("Ban chua nhap ho ten sinh vien \n");
            txtHoTen.setBackground(Color.red);
            JOptionPane.showMessageDialog(this, sb);
            return false;
        } else {
            txtHoTen.setBackground(Color.white);
        }
        return true;
    }
    public void fillData(){
        DefaultTableModel tbModel = (DefaultTableModel) tbTable.getModel();
        tbModel.setRowCount(0);
        for (SinhVien sv : svd.getAllSinhVien()) {
            Object[] rowData = new Object[7];
            rowData[0] = sv.getMaSinhVien();
            rowData[1] = sv.getHotenSinhVien();
            rowData[2] = sv.getEmail();
            rowData[3] = sv.getPhone();
            String gt = "Nu";
            if (sv.isGioitinh()) {
                gt = "Nam";
            }
            rowData[4] = gt;
            rowData[5] = sv.getDiachi();
            rowData[6] = sv.getHinhanh();
            tbModel.addRow(rowData);
        }
    }
    public void setModel(SinhVien sv){
        int rowId = tbTable.getSelectedRow();
        txtMaSinhVien.setText(sv.getMaSinhVien());
        txtHoTen.setText(sv.getHotenSinhVien());
        txtEmail.setText(sv.getEmail());
        txtPhone.setText(sv.getPhone()+"");
        boolean gt = sv.isGioitinh();
        if (gt) {
            rdNam.setSelected(true);
        } else {
            rdNu.setSelected(true);
        }
        txtDiaChi.setText(sv.getDiachi());
        ImageIcon icon = new ImageIcon(getClass().getResource("/images/Hinh_Anh/"+tbTable.getValueAt(rowId, 6).toString()));
        icon = new ImageIcon(icon.getImage().getScaledInstance(lblHinhanh.getWidth(), lblHinhanh.getHeight(), 0));
        lblHinhanh.setIcon(icon);
    }
