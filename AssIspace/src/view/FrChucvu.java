    public boolean check(){
        if (txtUsername.getText().isEmpty() || txtPassword.getText().isEmpty() || txtEnterPassword.getText().isEmpty()) {
            return false;
        }
        String pass1 = txtPassword.getText();
        String pass2 = txtEnterPassword.getText();
        if (pass1.equals(pass2)) {
            
            } else {
                
               JOptionPane.showMessageDialog(this, "Pass sai");
               return false;
            }
        return true;
        
    }
    public User getModel(){
        User u = new User();
        u.setUsername(txtUsername.getText().trim());
        u.setPassword(txtPassword.getText().trim());
        boolean cv = false;
        if (rdTruongPhong.isSelected()) {
            cv = true;
        }
        u.setRole(cv);
//        if (txtPassword.getText().equals(txtEnterPassword.getText())) {
//            return u;
//        }
//        return null;
        return u;
    }
    public void fillData(){
        DefaultTableModel tbModel = (DefaultTableModel) tbTable.getModel();
        tbModel.setRowCount(0);
        for (User u : udao.getAll()) {
            Object[] rowData = new Object[3];
            rowData[0] = u.getUsername();
            rowData[1] = u.getPassword();
            String cv = "Nhan Vien";
            if (u.isRole()) {
                cv = "Truong Phong";
            }
            rowData[2] = cv;
            tbModel.addRow(rowData);
        }
        
    }
