    public void resetForm(){
        txtId.setText("");
        txtName.setText("");
    }
    public boolean checkValidation(){
        if (txtId.getText().isEmpty() || txtName.getText().isEmpty()) {
            return false;
        }
        return true;
    }
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (!checkValidation()) {
            JOptionPane.showMessageDialog(this, "value is empty");
        } else {
            Category cate = new Category();
            cate.setId(Integer.parseInt(txtId.getText()));
            cate.setName(txtName.getText());

            if(cateDao.add(cate) > 0){
                System.out.println("add successful");
                System.out.println("count"+cateDao.count());
                lblCount.setText("COUNT: "+ cateDao.count());
                lblStatus.setText("Add successful");
                lblStatus.setForeground(Color.BLUE);
            }
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

