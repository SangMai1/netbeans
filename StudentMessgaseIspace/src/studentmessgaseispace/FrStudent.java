    StudentWeb setModel() throws ParseException{
        StudentWeb model = new StudentWeb();
        model.setId(Integer.parseInt(txtId.getText()));
        model.setName(txtName.getText());
        boolean gt = false;
        if (rdFemale.isSelected()) {
            gt = true;
        } 
        model.setGender(gt);
        model.setBirthay(DateHelper.toDate(txtBirthday.getText()));
        model.setEnglish(Double.parseDouble(txtEnglish.getText()));
        model.setSql(Double.parseDouble(txtSql.getText()));
        model.setPhp1(Double.parseDouble(txtPhp1.getText()));
        model.setPhp2(Double.parseDouble(txtPhp2.getText()));
        return model;
    }
