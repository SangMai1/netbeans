    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
       int id = Integer.parseInt(txtId.getText());
       String tensach = txtTensach.getText();
       String tacgia = txtTacgia.getText();
       String nxb = txtnxb.getText();
       String ngaymuon = txtNgayMuon.getText();
       String ngaytra = txtNgayTra.getText();
       double giatien = Double.parseDouble(txtGiaTien.getText());
       String theloai = "";
        if (rdcntt.isSelected()) {
            theloai = rdcntt.getText();
        } else if (rdkinhte.isSelected()) {
            theloai = rdkinhte.getText();
        } else {
            theloai = rdmarketting.getText();
        }
        String noidesach = cboNoiDeSach.getSelectedItem().toString();
        Sach s = new Sach(id, tensach, tacgia, nxb, giatien, ngaymuon, ngaytra, theloai, noidesach);
        if (sd.addSach(s) > 0) {
            model.addRow(new Object[] {
                s.getId(),s.getTensach(),s.getTacgia(),s.getNxb(),s.getGiatien(),s.getNgaymuon(),s.getNgaytra(),s.getTheloai(),s.getNoidesach()
            });
        }
    }//GEN-LAST:event_btnAddActionPerformed
