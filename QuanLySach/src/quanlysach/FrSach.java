    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtId.getText());
        Sach ss = sd.findSach(id);
        if (ss != null) {
            txtTenSach.setText(ss.getTensach());
            txtTacgia.setText(ss.getTacgia());
            txtnxb.setText(ss.getNxb());
            cboTang.setSelectedItem(ss.getTang());
            String theloai = ss.isTheloai();
            if (theloai == rdkinhdoanh.getText()) {
                rdkinhdoanh.setSelected(true);
            }
            else {
                if (theloai == rdLaptrinh.getText()) {
                    rdLaptrinh.setSelected(true);
                } else {
                    rdSale.setSelected(true);
                }
                
            }
        }
    }//GEN-LAST:event_btnFindActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if (!checkValition()) {
            JOptionPane.showMessageDialog(this, "value is emtry");
        } else {
            int id1 = tbTable.getSelectedRow();
            if (id1 > 0) {
                
                int id = Integer.parseInt(txtId.getText());
                String tensach = txtTenSach.getText();
                String tacgia = txtTacgia.getText();
                String nxb = txtnxb.getText();
                String theloai = "";
                if (rdkinhdoanh.isSelected()) {
                    theloai = rdkinhdoanh.getText();
                } else if (rdLaptrinh.isSelected()) {
                    theloai = rdLaptrinh.getText();
                } else {
                    theloai = rdSale.getText();
                }
                String sotang = cboTang.getSelectedItem().toString();
                Sach ss = new Sach(id, tensach, tacgia, nxb, theloai, sotang);
                model.setValueAt(id, id1, 0);
                model.setValueAt(tensach, id1, 1);
                model.setValueAt(tacgia, id1, 2);
                model.setValueAt(nxb, id1, 3);
                model.setValueAt(theloai, id1, 4);
                model.setValueAt(sotang, id1, 5);
                if (sd.editSach(ss) > 0) {
                    lblSatus.setText("edit successly");
                    lblCount.setText("COUNT:"+sd.count());
                    

                } else {
                    lblSatus.setText("ERROR");
                }
            }
            
        }
    }//GEN-LAST:event_btnEditActionPerformed
    public void filDatae(Sach s){
        txtId.setText(s.getId()+"");
        txtTenSach.setText(s.getTensach());
        txtTacgia.setText(s.getTacgia());
        txtnxb.setText(s.getNxb());
        String theloai = s.isTheloai();
        if (theloai == rdLaptrinh.getText()) {
            rdLaptrinh.setSelected(true);
        } else if (theloai == rdkinhdoanh.getText()) {
            rdkinhdoanh.setSelected(true);
        } else {
            rdSale.setSelected(true);
        }
        
    }
    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
        index++;
        Sach s = sd.getSach1(index);
        filDatae(s);
        if (tbTable.getSelectedRow() < tbTable.getRowCount()-1) {
            txtId.setText(tbTable.getValueAt(index, 0).toString());
            txtTenSach.setText(tbTable.getValueAt(index, 1).toString());
            txtTacgia.setText(tbTable.getValueAt(index, 2).toString());
            txtnxb.setText(tbTable.getValueAt(index, 3).toString());
            if (tbTable.getValueAt(index, 4).toString().equals("KINH DOANH")) {
                rdkinhdoanh.setSelected(true);
            } else if (tbTable.getValueAt(index, 4).toString().equals("LẬP TRÌNH")) {
                rdLaptrinh.setSelected(true);
            } else {
                rdSale.setSelected(true);
            }
            cboTang.setSelectedItem(tbTable.getValueAt(index, 5));
            tbTable.setRowSelectionInterval(index, index);
        }
    }//GEN-LAST:event_btnNextActionPerformed

