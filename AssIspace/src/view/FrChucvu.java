    private void tbTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbTableMouseClicked
        // TODO add your handling code here:
        int rowId = tbTable.rowAtPoint(evt.getPoint());
        txtUsername.setText(tbTable.getValueAt(rowId, 0).toString());
        txtPassword.setText(tbTable.getValueAt(rowId, 1).toString());
        txtEnterPassword.setText(tbTable.getValueAt(rowId, 1).toString());
        if (tbTable.getValueAt(rowId, 2).toString().equalsIgnoreCase("Truong Phong")) {
            rdTruongPhong.setSelected(true);
        } else {
            rdNhanVien.setSelected(true);
        }
    }//GEN-LAST:event_tbTableMouseClicked

