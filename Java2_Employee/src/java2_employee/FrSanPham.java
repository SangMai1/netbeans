    public SanPham getModel() throws ParseException{
        SanPham sp = new SanPham();
        sp.setMaSP(txtMaSP.getText().trim());
        sp.setTenSP(txtTenSP.getText().trim());
        sp.setGia(Double.parseDouble(txtGiaBan.getText().trim()));
        sp.setSoLuong(Integer.parseInt(txtSoLuong.getText().trim()));
        sp.setLoaiSP((LoaiSanPham)cboLoaiSanPham.getSelectedItem());
        sp.setNgayNhap(date_Format.parse(txtNgayNhap.getText()));
        return sp;
    }
    
    public void findData(){
        DefaultTableModel tbModel = (DefaultTableModel) tbSanPham.getModel();
        tbModel.setRowCount(0);//clear table
        for (SanPham sp : daoSP.getAllSanPham()) {
            Object[] rowDate = new Object[7];
            rowDate[0] = sp.getMaSP();
            rowDate[1] = sp.getTenSP();
            rowDate[2] = date_Format.format(sp.getNgayNhap());
            rowDate[3] = sp.getLoaiSP();
            rowDate[4] = sp.getSoLuong();
            rowDate[5] = sp.getGia();
            rowDate[6] = sp.getSoLuong() * sp.getGia();
            tbModel.addRow(rowDate);
        }
    }
    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if (!checkValidateForm()) {
            JOptionPane.showMessageDialog(this, "Ban chua nhap day du thong tin");
        } else {
            try {
                SanPham sp = getModel();
                System.out.println("=>"+sp.toString());
                if (btnLuu.getText().equals("LUU")) {
                    if (daoSP.add(sp) > 0) {
                        lblStatus.setText("Luu du lieu thanh cong");
                        lblStatus.setForeground(Color.red);
                        findData();
                    }
                } else {
                    if (daoSP.edit(sp) > 0) {
                        lblStatus.setText("Luu du lieu thanh cong");
                        lblStatus.setForeground(Color.red);
                        findData();
                    }
                }
            } catch (ParseException ex) {
                System.out.println("ERROR"+ex.toString());
            }
        }
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        if (txtMaSP.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ban chua nhap ma san pham");
        } else {
            if (daoSP.del(txtMaSP.getText().trim()) > 0) {
                lblStatus.setText("Da xoa thanh cong");
                lblStatus.setForeground(Color.BLUE);
                findData();
            } else {
                lblStatus.setText("k tim thay");
                lblStatus.setForeground(Color.red);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed
    public void setModel(SanPham sp){
        txtMaSP.setText(sp.getMaSP());
        txtGiaBan.setText(sp.getGia()+"");
        txtNgayNhap.setText(date_Format.format(sp.getNgayNhap()));
        txtSoLuong.setText(sp.getSoLuong()+"");
        txtTenSP.setText(sp.getTenSP());
        cboLoaiSanPham.setSelectedItem(sp.getLoaiSP());
    }
    
    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        if (txtMaSP.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ban chua nhap ma san pham tim kiem");
        } else {
            SanPham sp = daoSP.findByID(txtMaSP.getText().trim());
            if (sp != null) {
                lblStatus.setText("Da tim thay"+txtMaSP.getText());
                lblStatus.setForeground(Color.BLUE);
                //fill form 
                setModel(sp);
            } else {
                lblStatus.setText("k tim thay"+txtMaSP.getText().trim());
                lblStatus.setForeground(Color.RED);
            }
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

    private void tbSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSanPhamMouseClicked
        // TODO add your handling code here:
        int rowID = tbSanPham.rowAtPoint(evt.getPoint());
        SanPham sp = daoSP.getSanPhamAtPoint(rowID);
        setModel(sp);
        if (sp == null) {
            return;
        }
        setModel(sp);
        position = rowID;
        btnLuu.setText("SUA");
    }//GEN-LAST:event_tbSanPhamMouseClicked

