    public void reset(){
        txtId.setText("");
        txtName.setText("");
        rdNam.setSelected(true);
        txtBirthday.setText("");
        txtSalary.setText("");
        cboDefarment.setSelectedIndex(0);
    }
    public boolean check(){
        if (txtId.getText().isEmpty() || txtName.getText().isEmpty() || txtBirthday.getText().isEmpty() || txtSalary.getText().isEmpty()) {
            return false;
        }
        Float salary = Float.parseFloat(txtSalary.getText());
        if (salary < 100) {
            JOptionPane.showMessageDialog(this, "Salary khong duoc nho hon 100");
            return false;
        }
        return true;
    }
    public void getCombobox(){
        

        DefaultComboBoxModel<Phong> model = (DefaultComboBoxModel) cboDefarment.getModel();
        model.removeAllElements();
        for (Phong p : pdao.getPhong()) {
            model.addElement(p);
        }
    }
    public Employee getModel() throws ParseException{
        Employee ee = new Employee();
        ee.setId(txtId.getText().trim());
        ee.setName(txtName.getText().trim());
        boolean gt = false;
        if (rdNam.isSelected()) {
            gt = true;
        }
        ee.setGender(gt);
        ee.setBirthday(data_format.parse(txtBirthday.getText()));
        ee.setSalary(Float.parseFloat(txtSalary.getText()));
        Phong p = (Phong) cboDefarment.getSelectedItem();
        ee.setDeparment(p);
        return ee;
    }
    public void fillData(){
        DefaultTableModel tbModel = (DefaultTableModel) tbTable.getModel();
        tbModel.setRowCount(0);
        for ( Employee ee : edao.getEmployees()) {
            Object[] rowData = new Object[6];
            rowData[0] = ee.getId();
            rowData[1] = ee.getName();
            rowData[2] = ee.isGender();
            rowData[3] = data_format.format(ee.getBirthday());
            rowData[4] = ee.getSalary();
            rowData[5] = ee.getDeparment();
            tbModel.addRow(rowData);
        }
    }
    public void setModel(Employee ee){
        txtId.setText(ee.getId());
        txtName.setText(ee.getName());
        boolean gt = ee.isGender();
        if (gt) {
            rdNam.setSelected(true);
        } else {
            rdNu.setSelected(true);
        }
        txtBirthday.setText(data_format.format(ee.getBirthday()));
        txtSalary.setText(ee.getSalary()+"");
        cboDefarment.setSelectedItem(ee.getDeparment());
    }
    public void fillDataFind(String find){
        DefaultTableModel tbModel = (DefaultTableModel) tbTable.getModel();
        tbModel.setRowCount(0);
        for ( Employee ee : edao.getfind(find)) {
            Object[] rowData = new Object[6];
            rowData[0] = ee.getId();
            rowData[1] = ee.getName();
            rowData[2] = ee.isGender();
            rowData[3] = data_format.format(ee.getBirthday());
            rowData[4] = ee.getSalary();
            rowData[5] = ee.getDeparment();
            tbModel.addRow(rowData);
        }
    }
