    private void tbTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbTableMouseClicked
        // TODO add your handling code here:
        int rowData = tbTable.rowAtPoint(evt.getPoint());
        txtMaSinhVien.setText(tbTable.getValueAt(rowData, 0).toString());
        txtHoTen.setText(tbTable.getValueAt(rowData, 1).toString());
        txtEmail.setText(tbTable.getValueAt(rowData, 2).toString());
        txtPhone.setText(tbTable.getValueAt(rowData, 3).toString());
        if (tbTable.getValueAt(rowData, 4).toString().equalsIgnoreCase("Nam")) {
            rdNam.setSelected(true);
        } else {
            rdNu.setSelected(true);
        }
        txtDiaChi.setText(tbTable.getValueAt(rowData, 5).toString());
        ImageIcon icon = new ImageIcon(getClass().getResource("/images/Hinh_Anh/"+tbTable.getValueAt(rowData, 6).toString()));
        icon = new ImageIcon(icon.getImage().getScaledInstance(lblHinhanh.getWidth(), lblHinhanh.getHeight(), 0));
        lblHinhanh.setIcon(icon);
    }//GEN-LAST:event_tbTableMouseClicked

