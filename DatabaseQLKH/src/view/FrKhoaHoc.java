    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        rowId--;
        if (rowId <= 0) {
            rowId = 0;
        }
        KhoaHoc kh = getAtPointKhoaHoc(rowId);
        setModel(kh);
        String maChuyenDe = tbTableQuanLyKhoaHoc.getValueAt(rowId, 1).toString();
        cboChuyenDe.setSelectedIndex(chuyenDeSelect.get(maChuyenDe));
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
        rowId++;
        if (rowId >= khdao.getAllKhoaHoc().size()-1) {
            rowId = khdao.getAllKhoaHoc().size()-1;
        }
        KhoaHoc kh = getAtPointKhoaHoc(rowId);
        setModel(kh);
        String maChuyenDe = tbTableQuanLyKhoaHoc.getValueAt(rowId, 1).toString();
        cboChuyenDe.setSelectedIndex(chuyenDeSelect.get(maChuyenDe));
    }//GEN-LAST:event_btnNextActionPerformed

