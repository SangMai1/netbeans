    public boolean checkvalidate(){
        if (txttendangnhap.getText().isEmpty() || txtmatkhau.getText().isEmpty()) {
            return false;
        }
        return true;
    }
    private void btndangnhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndangnhapActionPerformed
        // TODO add your handling code here:
        if (checkvalidate()) {
           String u = txttendangnhap.getText();
           String p = new String(txtmatkhau.getPassword());
            if (sd.checkdangnhap(u, p)) {
                FrSinhVien fsv = new FrSinhVien();
                fsv.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Ban nhap sai u va p");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ban chua nhap thong tin");
        }
        
    }//GEN-LAST:event_btndangnhapActionPerformed

