    public void getCombobox(){
        DefaultComboBoxModel<NhaXuatBan> model = (DefaultComboBoxModel) cboNhaXuatBan.getModel();
        model.removeAllElements();
        int count = 0;
        sachSelect = new HashMap<>();
        for (NhaXuatBan nxb : xbdao.getAllNhaXuatBan()) {
            sachSelect.put(count, nxb.getTennxb());
            model.addElement(nxb);
           
        }
    }
    public Sach getModel(){
        Sach s = new Sach();
        s.setMasach(Integer.parseInt(txtMaSach.getText()));
        s.setTensach(txtTenSach.getText());
        s.setGia(Float.parseFloat(txtGia.getText()));
        s.setNgaynhap(DateHelper.toDate(txtNgayNhap.getText()));
        s.setMaxb((NhaXuatBan) cboNhaXuatBan.getSelectedItem());
        return s;
    }
    public void fillData(){
        DefaultTableModel tbModel = (DefaultTableModel) tbTableSach.getModel();
        tbModel.setRowCount(0);
        List<Sach> ls = sdao.getAllSach();
        for (Sach s : ls) {
            Object[] rowData = new Object[5];
            rowData[0] = s.getMasach();
            rowData[1] = s.getTensach();
            rowData[2] = s.getGia();
            rowData[3] = s.getNgaynhap();
            rowData[4] = s.getMaxb().getTennxb();
            System.out.println("aaaaaa"+s.getMaxb().getTennxb());
            tbModel.addRow(rowData);
        }
    }
    public Sach getAtPoint(int pos){
        Sach s = new Sach();
        s.setMasach(Integer.parseInt(tbTableSach.getValueAt(pos, 0).toString()));
        s.setTensach(tbTableSach.getValueAt(pos, 1).toString());
        s.setGia(Float.parseFloat(tbTableSach.getValueAt(pos, 2).toString()));
        s.setNgaynhap(DateHelper.toDate(tbTableSach.getValueAt(pos, 3).toString()));
        cboNhaXuatBan.setSelectedItem(xbdao.getByMa(Integer.parseInt(tbTableSach.getValueAt(pos, 4).toString())));
        if (tbTableSach.getValueAt(pos, 5).equals(s)) {
            
        }
        return s;
    }
    public void setModel(Sach s){
        txtMaSach.setText(s.getMasach()+"");
        txtTenSach.setText(s.getTensach());
        txtGia.setText(s.getGia()+"");
        txtNgayNhap.setText(DateHelper.toString(s.getNgaynhap()));

    }
