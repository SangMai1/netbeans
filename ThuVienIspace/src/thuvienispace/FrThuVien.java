    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int id1 = tbTable.getSelectedRow();
        if (id1 >= 0) {
            int id = Integer.parseInt(txtId.getText());
            String tensach = txtTensach.getText();
            String tacgia = txtTacgia.getText();
            String nxb = txtnxb.getText();
            double giatien = Double.parseDouble(txtGiaTien.getText());
            String theloai = "";
            if (rdcntt.isSelected()) {
                theloai = rdcntt.getText();
            } else if (rdkinhte.isSelected()) {
                theloai = rdkinhte.getText();
            } else {
                theloai = rdmarketting.getText();
            }
            String noidesach = cboNoiDeSach.getSelectedItem().toString();
            model.setValueAt(id, id1, 0);
            model.setValueAt(tensach, id1, 1);
            model.setValueAt(tacgia, id1, 2);
            model.setValueAt(nxb, id1, 3);
            model.setValueAt(giatien, id1, 4);
            model.setValueAt(theloai, id1, 5);
            model.setValueAt(noidesach, id1, 6);
        }
    }//GEN-LAST:event_btnEditActionPerformed

