    public void resetForm(){
        txtId.setText("");
        txtName.setText("");
        txthtml.setText("");
        txtphp.setText("");
    }
    public boolean check(){
        if (txtId.getText().isEmpty() || txtName.getText().isEmpty() || txthtml.getText().isEmpty() || txtphp.getText().isEmpty()) {
            return false;
        }
        return true;
    }
    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        Student std = new Student();
        std.setId(Integer.parseInt(txtId.getText()));
        std.setName(txtName.getText());
        std.setHtml(Integer.parseInt(txthtml.getText()));
        std.setPhp(Integer.parseInt(txtphp.getText()));
        if (stDao.edit(std) > 0) {
            lblStatus.setText("edit successly");
            lblCount.setText("count:"+stDao.count());
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtId.getText());
        Student st = stDao.findStudent(id);
        if (st != null) {
            lblStatus.setText("FOUND");
            txtName.setText(st.getName());
            txthtml.setText(st.getHtml()+"");
            txtphp.setText(st.getPhp()+"");
        } else {
            lblStatus.setText("NOT FOUND");
        }
    }//GEN-LAST:event_btnFindActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        Student std = new Student();
        std.setId(Integer.parseInt(txtId.getText()));
        std.setName(txtName.getText());
        std.setHtml(Integer.parseInt(txthtml.getText()));
        std.setPhp(Integer.parseInt(txtphp.getText()));
        if(stDao.addStudent(std) > 0){
            
            lblStatus.setText("add successly");
            lblCount.setText("COUNT:"+stDao.count());
            lblSum.setText("sum:"+std.sum());
            lblAvg.setText("avg:"+std.avg());
        }
    }//GEN-LAST:event_btnAddActionPerformed

