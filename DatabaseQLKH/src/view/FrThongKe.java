    public void fillData(){
        DefaultTableModel tbModel = (DefaultTableModel) tbTableThongKeNguoiHoc.getModel();
        tbModel.setRowCount(0);
        List<Object[]> list = tkdao.getAllNguoiHoc();
        for (Object[] row : list) {
            tbModel.addRow(row);
        }
    }
    public void getComboboxBanDiem(){
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboThongKeKhoaHoc.getModel();
        model.removeAllElements();
        List<KhoaHoc> ls = khdao.getAllKhoaHoc();
        for (KhoaHoc kh : ls) {
            model.addElement(kh);
        }
        cboThongKeKhoaHoc.setSelectedIndex(0);
    }
    public void fillDataBangDiem(){
        DefaultTableModel tbModel = (DefaultTableModel) tbTableThongKeBangDiem.getModel();
        tbModel.setRowCount(0);
        KhoaHoc kh = (KhoaHoc) cboThongKeKhoaHoc.getSelectedItem();
        List<Object[]> list = tkdao.getBangDiem(kh.getMaKH());
        for (Object[] row : list) {
            tbModel.addRow(row);
        }
    }
    public void fillDataTongHopDiem(){
        DefaultTableModel tbModel = (DefaultTableModel) tbTableThongKeTongHopDiem.getModel();
        tbModel.setRowCount(0);
        List<Object[]> ls = tkdao.getDiemChuyenDe();
        for (Object[] row : ls) {
            tbModel.addRow(row);
        }
    }
    public void getComboboxNam(){
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboThongKeDoanhThu.getModel();
        model.removeAllElements();
        List<KhoaHoc> ls = khdao.getAllKhoaHoc();
        for (KhoaHoc kh : ls) {
            int nam = kh.getNgayKhaiGiang().getYear()+1900;
            if (model.getIndexOf(nam) < 0) {
                model.addElement(nam);
            }
        }
    }
    public void fillDataDoanhThu(){
        DefaultTableModel tableModel = (DefaultTableModel) tbTableThongKeDoanhThu.getModel();
        tableModel.setRowCount(0);
        int nam = Integer.parseInt(cboThongKeDoanhThu.getSelectedItem().toString());
        List<Object[]> list = tkdao.getDoanhThu(nam);
        for (Object[] row : list) {
            tableModel.addRow(row);
        }
    }
