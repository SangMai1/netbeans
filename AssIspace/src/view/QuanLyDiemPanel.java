    public QuanLyDiem getModel(){
        QuanLyDiem ql = new QuanLyDiem();
        ql.setSv(new SinhVien(txtMaSV.getText(), txtTenSV.getText()));
        ql.setDiemTin(Float.parseFloat(txtDiemTinHoc.getText()));
        ql.setDiemAnhVan(Float.parseFloat(txtDiemAnhVan.getText()));
        ql.setDiemGDTC(Float.parseFloat(txtGiaoDucTC.getText()));
        return ql;
    }
    public void setModel(QuanLyDiem ql){
        SinhVien sv = new SinhVien();
        txtMaSV.setText(ql.getSv().getMaSinhVien());
        txtTenSV.setText(ql.getSv().getHotenSinhVien());
        txtDiemTinHoc.setText(String.valueOf(ql.getDiemTin()));
        txtDiemAnhVan.setText(String.valueOf(ql.getDiemAnhVan()));
        txtGiaoDucTC.setText(String.valueOf(ql.getDiemGDTC()));
        String dtb = String.format("%.2f", ql.getDiemTB());
        lblDiemTrungBinh.setText(dtb);
    }
    public QuanLyDiem getAtPointDiem(int pos){
        QuanLyDiem ql = new QuanLyDiem();
        SinhVien sv = new SinhVien();
        sv.setMaSinhVien(tbTable.getValueAt(pos, 0).toString());
        sv.setHotenSinhVien(tbTable.getValueAt(pos, 1).toString());
        ql.setSv(sv);
        ql.setDiemTin(Float.parseFloat(tbTable.getValueAt(pos, 2).toString()));
        ql.setDiemAnhVan(Float.parseFloat(tbTable.getValueAt(pos, 3).toString()));
        ql.setDiemGDTC(Float.parseFloat(tbTable.getValueAt(pos, 4).toString()));
        return ql;
    }
    private void btnLuulaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuulaiActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            QuanLyDiem q = getModel();
            if (dao.getQuanLyDiemByMaSV(q.getSv().getMaSinhVien()) != null) {
                JOptionPane.showMessageDialog(this, "ma sinh vien nay da ton tai");
            } else {
                if (dao.add(q) > 0) {
                    JOptionPane.showMessageDialog(this, "Luu thanh cong");
                    fillData();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Xin vui long kiem tra thong tin");
        }
    }//GEN-LAST:event_btnLuulaiActionPerformed
