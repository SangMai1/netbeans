    public ChuyenNganh getModel(){
        ChuyenNganh cn = new ChuyenNganh();
        cn.setMaCN(txtMaCN.getText());
        cn.setTenCN(txtTenCN.getText());
        return cn;
    }
    public void fillData(){
        DefaultTableModel tbModel = (DefaultTableModel) tbTableChuyenNganh.getModel();
        tbModel.setRowCount(0);
        for (ChuyenNganh cn : cndao.getAllChuyenNganh()) {
            Object[] rowData = new Object[2];
            rowData[0] = cn.getMaCN();
            rowData[1] = cn.getTenCN();
            tbModel.addRow(rowData);
        }
    }
    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        ChuyenNganh cn = getModel();
        if (cndao.add(cn) > 0) {
            JOptionPane.showMessageDialog(this, "add thanh cong");
            fillData();
        }
    }//GEN-LAST:event_btnLuuActionPerformed

