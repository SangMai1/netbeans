    public void getComboSinhVien(){
        DefaultComboBoxModel<SinhVien> model = (DefaultComboBoxModel) cboSinhVien.getModel();
        model.removeAllElements();
        for (SinhVien sv : svDAO.getAllSinhVien()) {
            model.addElement(sv);
        }
    }
    public void fillData(){
        DefaultTableModel tbModel = (DefaultTableModel) tbTable.getModel();
        tbModel.setRowCount(0);
        for (QuanLyDiem ql : dao.getAll()) {
            Object[] rowData = new Object[7];
            rowData[0] = ql.getSv().getMaSinhVien();
            rowData[1] = ql.getSv().getHotenSinhVien();
            rowData[2] = ql.getDiemTin();
            rowData[3] = ql.getDiemAnhVan();
            rowData[4] = ql.getDiemGDTC();
            String dtb = String.format("%.2f", ql.getDiemTB());
            rowData[5] = dtb;
            rowData[6] = ql.xepLoai();
            tbModel.addRow(rowData);
        }
    }
    public boolean validateForm(){
        if (txtMaSV.getText().isEmpty() || txtTenSV.getText().isEmpty() || txtDiemTinHoc.getText().isEmpty() || txtDiemAnhVan.getText().isEmpty() || txtGiaoDucTC.getText().isEmpty()) {
            return false;
        }
        return true;
    }
