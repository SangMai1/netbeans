    void fillData(){
        
        tbModel.setRowCount(0);
        for (Student st1 : sd.getAllStudents()) {
            StudentWeb st = (StudentWeb) st1;
            Object[] rowData = new Object[10];
            rowData[0] = st.getId();rowData[5] = st.getEnglish();
            rowData[1] = st.getName();rowData[6] = st.getPhp1();
            rowData[2] = st.isGender();rowData[7] = st.getPhp2();
            rowData[3] = st.getBirthay();rowData[8] = st.avg();
            rowData[4] = st.getSql();rowData[9] = st.arrangement(st.avg());
            tbModel.addRow(rowData);
        }
    }
    void fillDataFind(String find){
        
        tbModel.setRowCount(0);
        for (Student st1 : sd.getFind(find)) {
            StudentWeb st = (StudentWeb) st1;
            Object[] rowData = new Object[10];
            rowData[0] = st.getId();rowData[5] = st.getEnglish();
            rowData[1] = st.getName();rowData[6] = st.getPhp1();
            rowData[2] = st.isGender();rowData[7] = st.getPhp2();
            rowData[3] = st.getBirthay();rowData[8] = st.avg();
            rowData[4] = st.getSql();rowData[9] = st.arrangement(st.avg());
            tbModel.addRow(rowData);
        }
    }
